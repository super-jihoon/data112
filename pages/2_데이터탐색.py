import streamlit as st
import pandas as pd

st.set_page_config(
    page_title="데이터 탐색",
    page_icon="📊",
    layout="wide"
)

st.title("📊 데이터 탐색")

st.markdown("""
본 페이지에서는 분석에 사용되는 **수원시(2014~2024) 범죄 데이터**를 확인하고,
필터링 및 기초 통계를 통해 데이터의 특성을 살펴봅니다.
""")

st.divider()

# -----------------------------
# 데이터 불러오기
# -----------------------------

data = {
    "연도": [2014,2015,2016,2017,2018,2019,2020,2021,2022,2023,2024],
    "재산범죄":[86352,90864,86010,82816,89826,96935,105666,91984,98889,107671,125962],
    "강력범죄(폭력)":[34797,38662,39830,38326,37682,38872,35021,30105,32222,30231,28224],
    "강력범죄(흉악)":[4674,5005,5013,5250,5249,5102,5289,5739,6750,6231,5860]
}

df = pd.DataFrame(data)

# -----------------------------
# KPI
# -----------------------------

st.header("📌 데이터 개요")

col1, col2, col3, col4 = st.columns(4)

col1.metric("📍 지역", "수원시")
col2.metric("📅 기간", "2014~2024")
col3.metric("📊 데이터 수", len(df))
col4.metric("🚓 범죄 유형", "3종")

st.divider()

# -----------------------------
# 데이터 설명
# -----------------------------

st.header("📖 데이터 설명")

st.markdown("""
| 변수 | 설명 |
|------|------|
| 연도 | 범죄 발생 연도 |
| 재산범죄 | 절도, 사기 등 재산 관련 범죄 |
| 강력범죄(폭력) | 폭행, 상해 등 폭력범죄 |
| 강력범죄(흉악) | 살인, 강도, 강간 등 강력범죄 |
""")

st.divider()

# -----------------------------
# 필터
# -----------------------------

st.header("🎛 데이터 필터")

col1, col2 = st.columns(2)

with col1:

    years = st.slider(
        "연도 선택",
        2014,
        2024,
        (2014,2024)
    )

with col2:

    crimes = st.multiselect(
        "범죄 선택",
        ["재산범죄","강력범죄(폭력)","강력범죄(흉악)"],
        default=["재산범죄","강력범죄(폭력)","강력범죄(흉악)"]
    )

filtered = df[
    (df["연도"]>=years[0])&
    (df["연도"]<=years[1])
]

st.divider()

# -----------------------------
# 데이터 보기
# -----------------------------

st.header("📋 원본 데이터")

show = filtered[["연도"]+crimes]

st.dataframe(
    show,
    use_container_width=True,
    hide_index=True
)

st.divider()

# -----------------------------
# 기초 통계
# -----------------------------

st.header("📈 기초 통계")

st.dataframe(
    show.describe().round(2),
    use_container_width=True
)

st.divider()

# -----------------------------
# 데이터 품질
# -----------------------------

st.header("🔍 데이터 품질")

col1,col2=st.columns(2)

with col1:

    st.success(f"결측치 개수 : {df.isnull().sum().sum()}")

with col2:

    st.success(f"중복 데이터 : {df.duplicated().sum()}")

st.divider()

# -----------------------------
# 다운로드
# -----------------------------

st.header("💾 데이터 다운로드")

csv = show.to_csv(index=False).encode("utf-8-sig")

st.download_button(
    "📥 CSV 다운로드",
    csv,
    "suwon_crime.csv",
    "text/csv"
)

st.divider()

# -----------------------------
# 데이터 요약
# -----------------------------

st.header("📝 데이터 요약")

st.info("""
- 본 데이터는 **수원시 2014~2024년 범죄 발생 건수**를 기반으로 구성되었습니다.
- 분석 대상은 **재산범죄**, **강력범죄(폭력)**, **강력범죄(흉악)**입니다.
- 다음 페이지에서는 연도별 범죄 추이를 시각화하여 Pre-CAS 도입 전후의 변화를 분석합니다.
""")
