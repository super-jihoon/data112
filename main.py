import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt

# 제목
st.title("AI 기반 예측치안(프리카스) 도입 전후 수원시 범죄 변화 분석")
st.write(
    "2014~2024년 수원시 주요 범죄 발생 추이를 분석하고 "
    "2021년 프리카스 도입 전후 변화를 비교합니다."
)

# 데이터 입력
data = {
    "연도": [2014, 2015, 2016, 2017, 2018, 2019, 2020, 2021, 2022, 2023, 2024],
    "재산범죄": [86352, 90864, 86010, 82816, 89826, 96935, 105666, 91984, 98889, 107671, 125962],
    "강력범죄(폭력)": [34797, 38662, 39830, 38326, 37682, 38872, 35021, 30105, 32222, 30231, 28224],
    "강력범죄(흉악)": [4674, 5005, 5013, 5250, 5249, 5102, 5289, 5739, 6750, 6231, 5860]
}

df = pd.DataFrame(data)

# 프리카스 도입 여부 추가
df["프리카스"] = df["연도"].apply(
    lambda x: "도입 전(2014~2020)" if x <= 2020 else "도입 후(2021~2024)"
)

# 데이터 확인
st.subheader("1. 수원시 범죄 데이터")
st.dataframe(df)


# -----------------------------
# 연도별 변화 그래프
# -----------------------------

st.subheader("2. 범죄 유형별 연도 변화")

selected = st.multiselect(
    "비교할 범죄 유형 선택",
    ["재산범죄", "강력범죄(폭력)", "강력범죄(흉악)"],
    default=["재산범죄", "강력범죄(폭력)", "강력범죄(흉악)"]
)

fig, ax = plt.subplots(figsize=(10, 5))

for crime in selected:
    ax.plot(df["연도"], df[crime], marker="o", label=crime)

ax.axvline(2021, linestyle="--", label="프리카스 도입(2021)")
ax.set_xlabel("연도")
ax.set_ylabel("발생 건수")
ax.set_title("수원시 주요 범죄 발생 추이")
ax.legend()

st.pyplot(fig)


# -----------------------------
# 도입 전후 평균 비교
# -----------------------------

st.subheader("3. 프리카스 도입 전후 평균 비교")

comparison = (
    df.groupby("프리카스")
    [["재산범죄", "강력범죄(폭력)", "강력범죄(흉악)"]]
    .mean()
    .round(2)
)

st.dataframe(comparison)


# -----------------------------
# 변화율 계산
# -----------------------------

st.subheader("4. 범죄 유형별 변화율")

before = comparison.loc["도입 전(2014~2020)"]
after = comparison.loc["도입 후(2021~2024)"]

change = ((after - before) / before * 100).round(2)

result = pd.DataFrame({
    "변화율(%)": change
})

st.dataframe(result)


# -----------------------------
# 해석
# -----------------------------

st.subheader("5. 분석 결과 해석")

st.write("""
- 재산범죄, 강력범죄(폭력), 강력범죄(흉악)의 도입 전후 평균 발생 건수를 비교하였다.
- 감소한 범죄는 AI 기반 예측치안과 관련된 범죄 예방 효과 가능성을 보여준다.
- 그러나 범죄 발생 변화는 코로나19, 인구 변화, 경찰 활동 변화 등 다양한 요인의 영향을 받을 수 있으므로
  프리카스 도입만으로 범죄 감소 원인을 단정하기 어렵다.
""")
